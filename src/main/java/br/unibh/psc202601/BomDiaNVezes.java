public class BomDiaNVezes {
    public static void main(String[] args) {
        int contador = 0;

        System.out.println("Programa para exibir bom dia 30 x");

        while (contador < 30) {
            System.out.println("Bom dia!!!");
            contador++;
        }
    }
}
